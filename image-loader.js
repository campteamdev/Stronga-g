map.on("popupopen", async function (e) {
    setTimeout(async () => {
        const popup = e.popup._contentNode;
        const nameElement = popup.querySelector("div");
        if (!nameElement) return;

        const name = nameElement.textContent.trim();
        console.log(`üìÇ üîç Otwieranie popupu dla: ${name}`);

        const { sliderHTML, images } = await generateImageSlider(name);

        if (sliderHTML) {
            popup.insertAdjacentHTML("afterbegin", sliderHTML);
            console.log(`üìÇ ‚úÖ HTML slidera dodany do popupu dla: ${name}`);

            // Wymuszenie sprawdzenia obecno≈õci slidera
            setTimeout(() => {
                const safeSliderId = `swiper-container-${name
                    .trim()
                    .normalize("NFD").replace(/[\u0300-\u036f]/g, "") // Usuniƒôcie polskich znak√≥w
                    .replace(/[‚Äì‚Äî]/g, "-") // Zamiana d≈Çugiego my≈õlnika na zwyk≈Çy my≈õlnik
                    .replace(/[\s_]+/g, "-") // Zamiana spacji i podkre≈õle≈Ñ na my≈õlnik
                    .replace(/[^a-zA-Z0-9-]/g, "") // Usuniƒôcie innych znak√≥w specjalnych
                    .toLowerCase()}`;
                
                console.log(`üìÇ üìå Sprawdzam obecno≈õƒá slidera:`, document.querySelector(`.${safeSliderId}`));
                
            }, 500);

            initializeSwiper(name, images);
        }
    }, 300); // Drobne op√≥≈∫nienie na wygenerowanie popupu
});

