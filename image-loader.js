// üîπ Normalizacja nazw dla CSS i ID HTML
function sanitizeName(name) {
    return name
        .trim()
        .normalize("NFD").replace(/[\u0300-\u036f]/g, "") // Usuniƒôcie polskich znak√≥w
        .replace(/&/g, "and") // Zamiana `&` na "and"
        .replace(/[‚Äì‚Äî]/g, "-") // Zamiana d≈Çugiego i kr√≥tkiego my≈õlnika na zwyk≈Çy "-"
        .replace(/[_\s,./]+/g, "-") // Zamiana `_`, spacji, `,`, `.`, `/` na "-"
        .replace(/[^a-zA-Z0-9-]/g, "") // Usuniƒôcie pozosta≈Çych znak√≥w specjalnych
        .replace(/-+/g, "-") // Usuniƒôcie wielokrotnych my≈õlnik√≥w
        .toLowerCase(); // Zamiana na ma≈Çe litery
}

// üîπ Pobieranie zdjƒôƒá z GitHuba
async function getLocationImages(name) {
    const cacheKey = `images_${name}`;
    const cacheTimeKey = `cache_time_${name}`;

    const cachedData = localStorage.getItem(cacheKey);
    const cacheTime = localStorage.getItem(cacheTimeKey);
    const now = Date.now();

    if (cachedData && cacheTime && now - cacheTime < 15 * 60 * 1000) {
        console.log(`üìÇ üì• ≈Åadowanie zdjƒôƒá z cache: ${name}`);
        return JSON.parse(cachedData);
    }

    const githubRepo = "https://api.github.com/repos/campteamdev/Stronga-g/contents/";

    // üîπ Pobranie listy folder√≥w z repozytorium
    let folders = [];
    try {
        const repoResponse = await fetch(githubRepo);
        if (!repoResponse.ok) {
            console.error("‚ùå B≈ÇƒÖd pobierania folder√≥w z GitHuba:", repoResponse.statusText);
            return [];
        }
        folders = await repoResponse.json();
        folders = folders.filter(item => item.type === "dir").map(item => item.name);
        console.log("üìÇ ‚úÖ Lista folder√≥w w repozytorium:", folders);
    } catch (error) {
        console.error("‚ùå B≈ÇƒÖd pobierania folder√≥w:", error);
        return [];
    }

    // üîπ Funkcja normalizujƒÖca nazwƒô folderu do por√≥wnywania
    function normalizeName(str) {
        return str
            .trim() // Usuniƒôcie spacji na poczƒÖtku i ko≈Ñcu
            .normalize("NFD").replace(/[\u0300-\u036f]/g, "") // Usuniƒôcie polskich znak√≥w
            .replace(/[‚Äì-]+/g, " ") // Zamiana d≈Çugiego i kr√≥tkiego my≈õlnika na spacjƒô
            .replace(/_/g, " ") // Zamiana podkre≈õlenia `_` na spacjƒô
            .replace(/\s+/g, " ") // Usuniƒôcie podw√≥jnych spacji
            .toLowerCase(); // Zamiana na ma≈Çe litery
    }

    const baseName = normalizeName(name);

    // üîπ Znalezienie najlepszego dopasowania folderu w repozytorium
    let matchedFolder = folders.find(folder => normalizeName(folder) === baseName);

    if (!matchedFolder) {
        console.warn(`‚ö†Ô∏è Folder dla "${name}" nie znaleziony.`);
        return [];
    }

    console.log(`üìÇ üîç U≈ºywam folderu: "${matchedFolder}"`);

    let images = [];

    try {
        const response = await fetch(`${githubRepo}${encodeURIComponent(matchedFolder)}`);
        if (!response.ok) {
            console.warn(`‚ö†Ô∏è Folder "${matchedFolder}" nie zosta≈Ç znaleziony.`);
            return [];
        }

        const data = await response.json();
        images = data
            .filter(file => file.download_url && /\.(jpg|jpeg|webp)$/i.test(file.name))
            .map(file => file.download_url)
            .slice(0, 10);

        if (images.length > 0) {
            console.log(`‚úÖ Zdjƒôcia dla "${name}":`, images);
            localStorage.setItem(cacheKey, JSON.stringify(images));
            localStorage.setItem(cacheTimeKey, Date.now());
        } else {
            console.warn(`‚ö†Ô∏è Brak zdjƒôƒá w folderze "${matchedFolder}".`);
        }
    } catch (error) {
        console.error(`‚ùå B≈ÇƒÖd pobierania zdjƒôƒá z folderu "${matchedFolder}":`, error);
    }

    return images;
}


// üîπ Funkcja inicjalizujƒÖca Swiper
function initializeSwiper(name, images) {
    const safeName = sanitizeName(name);
const sliderId = `.swiper-container-${safeName}`;
const prevBtnId = `#swiper-prev-${safeName}`;
const nextBtnId = `#swiper-next-${safeName}`;

    
    setTimeout(() => {
        const swiper = new Swiper(sliderId, {
            loop: false,
            autoplay: false,
            pagination: { el: `${sliderId} .swiper-pagination`, clickable: true },
            slidesPerView: 1,
            spaceBetween: 10,
            navigation: {
                nextEl: nextBtnId,
                prevEl: prevBtnId
            }
        });

        console.log(`‚úÖ Swiper zainicjalizowany dla ${name}`);

        // üîπ Obs≈Çuga powiƒôkszenia zdjƒôcia
        document.querySelectorAll(`${sliderId} .zoomable-image`).forEach((img, index) => {
            img.addEventListener("click", () => openFullscreen(images, index));
        });

    }, 500);
}

async function generateImageSlider(name) {
    const images = await getLocationImages(name);
    if (images.length === 0) return "";

    console.log(`‚úÖ Generowanie slidera dla: ${name} (${images.length} zdjƒôƒá)`);

    // U≈ºycie funkcji normalizeName do bezpiecznych identyfikator√≥w HTML/CSS
    const safeName = sanitizeName(name);
    const sliderId = `swiper-container-${safeName}`;
    const prevBtnId = `swiper-prev-${safeName}`;
    const nextBtnId = `swiper-next-${safeName}`;

    const sliderHTML = `
        <div class="swiper-container ${sliderId}" style="width:100%; height: 150px; position: relative; overflow: hidden;">
            <div class="swiper-wrapper">
                ${images.map(img => `
                    <div class="swiper-slide">
                        <img src="${img}" class="zoomable-image" style="width:100%; height:150px; object-fit:cover; border-radius:8px; cursor:pointer;">
                    </div>
                `).join("")}
            </div>
            <div class="swiper-pagination"></div>
            <div id="${prevBtnId}" class="custom-swiper-prev">‚ùÆ</div>
            <div id="${nextBtnId}" class="custom-swiper-next">‚ùØ</div>
        </div>
    `;

    console.log(`üìÇ ‚úÖ Wygenerowany kod HTML dla ${name}:`, sliderHTML);
    
    return { sliderHTML, images };
}



